/// Class containing all elevation tokens for the Nova design system
class NovaElevations {
  // Base elevation
  static const double base = 1.0;

  // Small elevations
  static const double xs = base; // 1
  static const double sm = base * 2; // 2
  static const double md = base * 4; // 4

  // Medium elevations
  static const double lg = base * 8; // 8
  static const double xl = base * 16; // 16
  static const double xxl = base * 24; // 24

  // Large elevations
  static const double xxxl = base * 32; // 32
  static const double xxxxl = base * 48; // 48
  static const double xxxxxl = base * 64; // 64

  // Special elevations
  static const double none = 0;
  static const double max = 9999;
} 