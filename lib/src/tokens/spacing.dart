/// Class containing all spacing tokens for the Nova design system
class NovaSpacing {
  // Base spacing
  static const double base = 4.0;

  // Extra small spacing
  static const double xs = base * 1; // 4
  static const double xs2 = base * 2; // 8
  static const double xs3 = base * 3; // 12

  // Small spacing
  static const double sm = base * 4; // 16
  static const double sm2 = base * 5; // 20
  static const double sm3 = base * 6; // 24

  // Medium spacing
  static const double md = base * 8; // 32
  static const double md2 = base * 10; // 40
  static const double md3 = base * 12; // 48

  // Large spacing
  static const double lg = base * 16; // 64
  static const double lg2 = base * 20; // 80
  static const double lg3 = base * 24; // 96

  // Extra large spacing
  static const double xl = base * 32; // 128
  static const double xl2 = base * 40; // 160
  static const double xl3 = base * 48; // 192

  // 2xl spacing
  static const double xxl = base * 64; // 256
  static const double xxl2 = base * 80; // 320
  static const double xxl3 = base * 96; // 384

  // 3xl spacing
  static const double xxxl = base * 128; // 512
  static const double xxxl2 = base * 160; // 640
  static const double xxxl3 = base * 192; // 768

  // 4xl spacing
  static const double xxxxl = base * 256; // 1024
  static const double xxxxl2 = base * 320; // 1280
  static const double xxxxl3 = base * 384; // 1536
} 