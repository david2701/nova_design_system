/// Class containing all opacity tokens for the Nova design system
class NovaOpacities {
  // Base opacity
  static const double base = 0.1;

  // Small opacities
  static const double xs = base; // 0.1
  static const double sm = base * 2; // 0.2
  static const double md = base * 3; // 0.3

  // Medium opacities
  static const double lg = base * 4; // 0.4
  static const double xl = base * 5; // 0.5
  static const double xxl = base * 6; // 0.6

  // Large opacities
  static const double xxxl = base * 7; // 0.7
  static const double xxxxl = base * 8; // 0.8
  static const double xxxxxl = base * 9; // 0.9

  // Special opacities
  static const double none = 0;
  static const double full = 1;
} 