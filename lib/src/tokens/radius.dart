/// Class containing all border radius tokens for the Nova design system
class NovaRadius {
  // Base radius
  static const double base = 4.0;

  // Small radius
  static const double xs = base * 0.5; // 2
  static const double sm = base; // 4
  static const double md = base * 2; // 8

  // Medium radius
  static const double lg = base * 3; // 12
  static const double xl = base * 4; // 16
  static const double xxl = base * 6; // 24

  // Large radius
  static const double xxxl = base * 8; // 32
  static const double xxxxl = base * 12; // 48
  static const double xxxxxl = base * 16; // 64

  // Special radius
  static const double none = 0;
  static const double full = 9999;
} 